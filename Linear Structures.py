# Create a list of integers
my_list = [1, 2, 3, 4, 5]

# Access the first element of the list
first_element = my_list[0]

# Access the last element of the list
last_element = my_list[-1]

# Append a new element to the end of the list
my_list.append(6)

# Insert a new element at a specific index
my_list.insert(3, 7)

# Remove an element from the list
my_list.remove(2)

# Get the length of the list
list_length = len(my_list)

# Check if an element is in the list
element_is_in_list = 5 in my_list

# Print the resulting list and some of its properties
print(my_list)
print(f"The first element is {first_element}")
print(f"The last element is {last_element}")
print(f"The list has {list_length} elements")
print(f"Is 5 in the list? {element_is_in_list}")
